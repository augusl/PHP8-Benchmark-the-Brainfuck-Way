class Brainfuck {

    compileBf(input){
        let pc      = 0
        let jmpPc   = 0
        const jmpStack = []
        const program  = []

        for(let c of input){
            switch(c){
                case '>':
                    program.push([1, 0])
                    break
                case '<':
                    program.push([2, 0])
                    break
                case '+':
                    program.push([3, 0])
                    break
                case '-':
                    program.push([4,0])
                    break
                case '.':
                    program.push([5,0])
                    break
                case ',':
                    program.push([6,0])
                    break
                case '[':
                    program.push([7,0])
                    jmpStack.push(pc)
                    break
                case ']':
                    if(!jmpStack.length){
                        throw new Error("Invalid Program")
                    }
                    jmpPc = jmpStack.pop()
                    program.push([8,jmpPc])
                    program[jmpPc][1] = pc
                    break
                default:
                    pc--
                    break
            }
            pc++
        }
        if(jmpStack.length){
            throw new Error("Invalid Program")
        }
        return program
    }

    executeBf(program){
        const data = new Array(65536).fill(0);
        let dataPtr = 0
        let pc
        let program_len = program.length
        for(pc = 0; pc < program_len; pc++){
            const [c, val] = program[pc]
            switch(c){
                case 1:
                    dataPtr++
                    break
                case 2:
                    dataPtr--
                    break
                case 3:
                    data[dataPtr]++
                    break
                case 4:
                    data[dataPtr]--
                    break
                case 5:
                    process.stdout.write(String.fromCharCode(data[dataPtr]))
                    break
                case 6:
                    // Disabled to make it synchronized
                    //data[dataPtr] = process.stdin.read(1).charCodeAt()
                    data[dataPtr] = 0
                    break
                case 7:
                    if(data[dataPtr] == 0){
                        pc = val
                    }
                    break
                case 8:
                    if(data[dataPtr] > 0){
                        pc = val
                    }
                    break
                default:
                    throw new Error("Unknown operator")
            }
        }
    }
}


