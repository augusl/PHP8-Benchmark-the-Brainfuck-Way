class Brainfuck {

    // operators
    const op_inc_dp  = 1;
    const op_dec_dp  = 2;
    const op_inc_val = 3;
    const op_dec_val = 4;
    const op_out     = 5;
    const op_in      = 6;
    const op_jmp_fwd = 7;
    const op_jmp_bck = 8;

    const operator   = 0;
    const operand    = 1;

    public static function stty($options) {
        exec($cmd = "/bin/stty $options", $output, $el);
        $el AND die("exec($cmd) failed");
        return implode(" ", $output);
    }

   public static function getchar($echo = false) {
        $echo = $echo ? "" : "-echo";

        # Get original settings
        $stty_settings = preg_replace("#.*; ?#s", "", self::stty("--all"));

        # Set new ones
        stty("cbreak $echo");

        # Get characters until a PERIOD is typed, 
        # showing their hexidecimal ordinal values.
        printf("> ");
        do {
            printf("%02x ", ord($c = fgetc(STDIN)));
        } while ($c != '.');

        # Return settings
        stty($stty_settings);
   }

    public function complieBf($input) {
        $pc      = 0;
        $jmpPc   = 0;
        $jmpStack = array();
        $program  = array(
            // operator,
            // operand
        );
        $inputLen = strlen($input);
        for($i=0;$i<$inputLen;$i++){
            $c = $input[$i];
            switch ($c) {
                case '>':
                    $program[] = array(self::op_inc_dp, 0);
                    break;
                case '<':
                    $program[] = array(self::op_dec_dp, 0);
                    break;
                case '+':
                    $program[] = array(self::op_inc_val, 0);
                    break;
                case '-':
                    $program[] = array(self::op_dec_val, 0);
                    break;
                case '.':
                    $program[] = array(self::op_out, 0);
                    break;
                case ',':
                    $program[] = array(self::op_in, 0);
                    break;
                case '[':
                    $program[] = array(self::op_jmp_fwd, 0);
                    $jmpStack[] = $pc;
                    break;
                case ']':
                    if(count($jmpStack) === 0){
                        return null;
                    }
                    $jmpPc = $jmpStack[count($jmpStack)-1];
                    array_pop($jmpStack);
                    $program[] = array(self::op_jmp_bck, $jmpPc);
                    $program[$jmpPc][self::operand] = $pc;
                    break;
                default:
                    $pc--;
                    break;
            }
            $pc++;
        }
        if(count($jmpStack) !== 0) {
            return null;
        }
        return $program;
    } 

    public function executeBf($program) {
        $data = array();
        $dataPtr = 0;
        for($i=0;$i<65535;$i++){
            $data[$i] = 0;
        }
        for($pc = 0; $pc < count($program); $pc++){
            // if(empty($data[$dataPtr])) $data[$dataPtr] = 0;
            switch ($program[$pc][self::operator]) {
                case self::op_inc_dp:
                    $dataPtr++;
                    break;
                case self::op_dec_dp:
                    $dataPtr--;
                    break;
                case self::op_inc_val:
                    $data[$dataPtr]++;
                    break;
                case self::op_dec_val:
                    $data[$dataPtr]--;
                    break;
                case self::op_out:
                    printf(chr($data[$dataPtr]));
                    break;
                case self::op_in:
                    $data[$dataPtr] = (int)self::getchar(); // if you want to use "op_in", please run this scripts on linux.
                    break;
                case self::op_jmp_fwd:
                    if ($data[$dataPtr] === 0) {
                        $pc = (int)$program[$pc][self::operand];
                    }
                    break;
                case self::op_jmp_bck:
                    if ($data[$dataPtr] > 0) {
                        $pc = (int)$program[$pc][self::operand];
                    }
                    break;
                default:
                    exit("Unknown operator: ".$program[$pc][self::operator]."\n");
                    break;
            }
        }

    }

}


