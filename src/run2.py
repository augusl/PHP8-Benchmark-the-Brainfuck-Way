import sys


class Brainfuck:
    # operators
    op_inc_dp  = 1
    op_dec_dp  = 2
    op_inc_val = 3
    op_dec_val = 4
    op_out     = 5
    op_in      = 6
    op_jmp_fwd = 7
    op_jmp_bck = 8

    operator   = 0
    operand    = 1

    def compileBf(self, input):
        pc      = 0
        jmpPc   = 0
        jmpStack = []
        program  = []

        for c in input:
            if c == '>':
                program.append((1, 0))
            elif c == '<':
                program.append((2, 0))
            elif c == '+':
                program.append((3, 0))
            elif c == '-':
                program.append((4, 0))
            elif c == '.':
                program.append((5, 0))
            elif c == ',':
                program.append((6, 0))
            elif c == '[':
                program.append((7, 0))
                jmpStack.append(pc)
            elif c == ']':
                if not jmpStack:
                    raise ValueError("Invalid Program")
                jmpPc = jmpStack.pop()
                program.append((8, jmpPc))
                program[jmpPc] = (program[jmpPc][0], pc)
            else:
                pc -= 1
            pc += 1
        if jmpStack:
            raise ValueError("Invalid Program")
        return program

    def executeBf(self, program):
        data = [0] * 65535
        dataPtr = 0
        pc = 0
        program_len = len(program)
        while pc < program_len:
            c, val = program[pc]
            #print("pc:", pc, "c:", c, "val:", val)
            if c == 1:
                dataPtr += 1
            elif c == 2:
                dataPtr -= 1
            elif c == 3:
                data[dataPtr] += 1
            elif c == 4:
                data[dataPtr] -= 1
            elif c == 5:
                sys.stdout.write(chr(data[dataPtr]))
            elif c == 6:
                data[dataPtr] = sys.stdin.buffer.read(1)[0]
            elif c == 7:
                if data[dataPtr] == 0:
                    pc = val
            elif c == 8:
                if data[dataPtr] > 0:
                    pc = val
            else:
                raise ValueError("Unknown operator")
            pc += 1


